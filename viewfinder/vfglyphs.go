// VFGlyphs6x13 is a 'hand drawn' basicfont.Face containing viewfinder glyphs. There is no separation between the glyphs
// so multiple characters can be used in sequence to create one glyph.
// For example, drawing a battery indicator where the battery is at 2/3 capacity, one would use the sequence "bCT".
//
// The VFGlyphs6x13 data is entirely self-contained and does not require loading from separate files.
//
// Supported icons
//
// - 3 bars battery indicator:
//   3/3 = "BAT"
//   2/3 = "bCT"
//   1/3 = "baU"
//   0/3 = "bat"
//
// - Capture delay:
//   2 seconds stopwatch: "uv"
//   10 seconds stopwatch: "uw"
//
// - Exposure compensation dial:
//   +/- icon = "+-"
//   dial base = "6..5..4..0..1..2..3"
//   empty number marker (for use when the indicator is on a whole number) = '"'
//   indicator = '!'
//
// - Exposure program mode icons:
//   P (auto)          = "Pp"
//   M (manual)        = "Mm"
//   S (shutter prio)  = "Nn"
//   A (aperture prio) = "Ll"
//
// - Film simulation:
//   Standard/Provia (STD) = "()*"
//   Velvia (V)            = "#$%"
//   Astia/Soft (S)        = "(&%"
//   Classic Chrome (Cc)   = ":,/"
//   PRO Neg. Hi (Nh)      = "=>;"
//   PRO Neg. Std (Ns)     = "=><"
//   B&W (B)               = "'&%"
//   Monochrome + Ye (By)  = "'?@"
//   Monochrome + R (Br)   = "'?7"
//   Monochrome + G (Bg)   = "'?8"
//   Sepia (SEPIA)         = "9DE"
//
// - Image quality:
//   RAW = "fgh"
//   S   = "jko"
//   M   = "jqo"
//   L   = "jro"
//
// - ISO icons:
//   AUTO (all caps black on white)    = "ISO"
//   iso (all lowercase white letters) = "is"
//
// - White balance:
//   daylight (sun)                   = "XY"
//   shade (sun cloud)                = "de"
//   fluorescent 1 (tl with rays + 1) = "FGH"
//   fluorescent 2 (tl with rays + 2) = "FGJ"
//   fluorescent 3 (tl with rays + 3) = "FGK"
//   incandescent (bulb with rays)    = "QR"
//   underwater (fish bubbles)        = "VW"
//   custom                           = "Z["
//   colour temperature               = "]^"
